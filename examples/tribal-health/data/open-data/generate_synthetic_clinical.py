"""
Synthetic Clinical Data Generator (Tribal Health)

Generates ENTIRELY SYNTHETIC clinical data aligned with IHS/tribal
health analytics patterns. NO real patient data is used or referenced.
All generated data is FHIR-aligned in structure and uses published
IHS disease prevalence rates for realistic distributions.

Outputs CSV files aligned with tribal-health bronze dbt models:
  - patient_demographics.csv  (brz_patient_demographics)
  - encounters.csv            (brz_encounters)
  - facilities.csv            (brz_facilities)

Usage:
    python generate_synthetic_clinical.py --patients 500 --encounters 2000
    python generate_synthetic_clinical.py --dataset patients --count 1000
    python generate_synthetic_clinical.py --dataset encounters --count 5000 --start-date 2023-01-01
    python generate_synthetic_clinical.py --dataset facilities
    python generate_synthetic_clinical.py --dataset all --output-dir ./data --seed 42
    python generate_synthetic_clinical.py --help

IMPORTANT: ALL data generated by this tool is ENTIRELY SYNTHETIC.
No real Protected Health Information (PHI) is generated, stored, or used.
This tool is designed for development, testing, and demonstration purposes only.
"""

