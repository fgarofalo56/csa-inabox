#!/usr/bin/env node
/**
 * local-golden-path — one command to bring the CSA Loom console up locally
 * in an honest, minimal "config-only demo" profile (WS-J / J1).
 * ---------------------------------------------------------------------------
 * WHAT THIS IS (and honestly is NOT)
 *
 * The Loom console is a Next.js BFF that talks to REAL Azure backends
 * (Synapse, ADX, Cosmos, Purview, AOAI, …). There is no local emulator that
 * reproduces that estate, so this profile does NOT fake data. Instead it gives
 * a new contributor the fastest real, working local run:
 *
 *   - `next dev` boots with a locally-generated SESSION_SECRET so the session
 *     crypto works exactly like production.
 *   - A `loom_session` cookie is minted locally (same HKDF+AES-256-GCM scheme
 *     as lib/auth/session.ts) so you are SIGNED IN as a local dev identity —
 *     no Entra/MSAL round-trip needed.
 *   - Every UI surface (shell, navigation, catalog, editors, wizards) renders
 *     and is navigable. Panels that need an Azure backend show their designed,
 *     honest "not configured" MessageBar gate (no-vaporware.md) — that IS the
 *     supported unconfigured state, not an error.
 *   - To light up live data, set the relevant LOOM_* vars in .env.local (see
 *     docs/fiab/local-golden-path.md); the same UI then calls the real backend.
 *
 * USAGE
 *   node scripts/dev/local-golden-path.mjs                # prepare, self-test, then `next dev`
 *   node scripts/dev/local-golden-path.mjs --prepare-only # prepare + self-test, do not start the server
 *   node scripts/dev/local-golden-path.mjs --start        # same as default (explicit)
 *
 * It never overwrites existing values in apps/fiab-console/.env.local.
 */

import fs from 'node:fs';
import path from 'node:path';
import crypto from 'node:crypto';
import { spawn, spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..', '..');
const CONSOLE_ROOT = path.join(REPO_ROOT, 'apps', 'fiab-console');
const ENV_LOCAL = path.join(CONSOLE_ROOT, '.env.local');
const AUTH_DIR = path.join(CONSOLE_ROOT, '.auth');
const AUTH_STATE = path.join(AUTH_DIR, 'loom-local-state.json');

const PREPARE_ONLY = process.argv.includes('--prepare-only');
const LOCAL_URL = 'http://localhost:3000';
const TTL_SECS = 8 * 60 * 60; // 8h, matches MAX_AGE_SECS default

// A fixed, obviously-local sentinel admin group id. Baked into both the
// minted session claims and LOOM_TENANT_ADMIN_GROUP_ID so the local dev user
// sees the full (admin) navigation. It is not a real Entra group.
const LOCAL_ADMIN_GROUP = 'loom-local-dev-admins';

function log(msg) { console.log(msg); }
function step(msg) { console.log(`\n> ${msg}`); }

// --- session crypto: identical to apps/fiab-console/lib/auth/session.ts -----
const ALG = 'aes-256-gcm';
const IV_LEN = 12;
const HKDF_INFO = 'loom-session-v1';
const COOKIE_NAME = 'loom_session';

function deriveKey(secret) {
  const ab = crypto.hkdfSync('sha256', Buffer.from(secret, 'utf-8'), Buffer.alloc(32), Buffer.from(HKDF_INFO), 32);
  return Buffer.from(ab);
}

function mintCookie(secret, claims, ttlSecs) {
  const key = deriveKey(secret);
  const payload = { claims, exp: Math.floor(Date.now() / 1000) + ttlSecs };
  const iv = crypto.randomBytes(IV_LEN);
  const cipher = crypto.createCipheriv(ALG, key, iv);
  const pt = Buffer.from(JSON.stringify(payload), 'utf-8');
  const enc = Buffer.concat([cipher.update(pt), cipher.final()]);
  const tag = cipher.getAuthTag();
  return Buffer.concat([iv, tag, enc]).toString('base64url');
}

function decodeCookie(secret, value) {
  const key = deriveKey(secret);
  const raw = Buffer.from(value, 'base64url');
  const iv = raw.subarray(0, IV_LEN);
  const tag = raw.subarray(IV_LEN, IV_LEN + 16);
  const enc = raw.subarray(IV_LEN + 16);
  const decipher = crypto.createDecipheriv(ALG, key, iv);
  decipher.setAuthTag(tag);
  const pt = Buffer.concat([decipher.update(enc), decipher.final()]);
  return JSON.parse(pt.toString('utf-8'));
}

// --- .env.local parsing / seeding -------------------------------------------
function parseEnvFile(file) {
  const out = {};
  let text;
  try { text = fs.readFileSync(file, 'utf8'); } catch { return out; }
  for (const line of text.split(/\r?\n/)) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq === -1) continue;
    out[t.slice(0, eq).trim()] = t.slice(eq + 1).trim();
  }
  return out;
}

const ENV_HEADER = `# CSA Loom — LOCAL GOLDEN-PATH profile (generated by scripts/dev/local-golden-path.mjs).
# This file is gitignored (.env.*). It configures an honest "config-only demo"
# local run: the console boots + you are signed in as a local dev identity, and
# Azure-backed panels show their designed "not configured" gates until you add
# the relevant LOOM_* vars below. See docs/fiab/local-golden-path.md.
`;

/**
 * Ensure .env.local exists and contains the minimal keys. Never overwrites an
 * existing value; only appends the ones that are missing. Returns the effective
 * env map after seeding.
 */
function ensureEnvLocal() {
  const existing = fs.existsSync(ENV_LOCAL) ? parseEnvFile(ENV_LOCAL) : null;
  const seeds = {
    SESSION_SECRET: () => crypto.randomBytes(32).toString('base64url'),
    LOOM_CLOUD: () => 'AzureCloud',
    AZURE_CLOUD: () => 'AzurePublicCloud',
    LOOM_TENANT_ADMIN_GROUP_ID: () => LOCAL_ADMIN_GROUP,
  };

  if (!existing) {
    const lines = [ENV_HEADER.trimEnd(), ''];
    for (const [k, gen] of Object.entries(seeds)) lines.push(`${k}=${gen()}`);
    lines.push('');
    lines.push('# --- Optional: uncomment + fill to light up live Azure-backed data ---');
    lines.push('# LOOM_SUBSCRIPTION_ID=');
    lines.push('# LOOM_SYNAPSE_WORKSPACE=');
    lines.push('# LOOM_COSMOS_ENDPOINT=');
    lines.push('# LOOM_PURVIEW_ACCOUNT=');
    lines.push('');
    fs.mkdirSync(CONSOLE_ROOT, { recursive: true });
    fs.writeFileSync(ENV_LOCAL, lines.join('\n'), 'utf8');
    log(`  created ${path.relative(REPO_ROOT, ENV_LOCAL)} with a generated SESSION_SECRET + demo defaults`);
    return parseEnvFile(ENV_LOCAL);
  }

  // Append only missing keys.
  const missing = Object.entries(seeds).filter(([k]) => !(k in existing));
  if (missing.length) {
    const appended = ['', '# --- appended by local-golden-path.mjs (missing keys) ---'];
    for (const [k, gen] of missing) appended.push(`${k}=${gen()}`);
    fs.appendFileSync(ENV_LOCAL, appended.join('\n') + '\n', 'utf8');
    log(`  updated ${path.relative(REPO_ROOT, ENV_LOCAL)} — added: ${missing.map(([k]) => k).join(', ')}`);
  } else {
    log(`  ${path.relative(REPO_ROOT, ENV_LOCAL)} already has the required keys — left untouched`);
  }
  return parseEnvFile(ENV_LOCAL);
}

function writeStorageState(cookieValue) {
  const expires = Math.floor(Date.now() / 1000) + TTL_SECS;
  const state = {
    cookies: [
      {
        name: COOKIE_NAME,
        value: cookieValue,
        domain: 'localhost',
        path: '/',
        expires,
        httpOnly: true,
        secure: false, // local http://localhost — keep the cookie sendable over plain http
        sameSite: 'Lax',
      },
    ],
    origins: [],
  };
  fs.mkdirSync(AUTH_DIR, { recursive: true });
  fs.writeFileSync(AUTH_STATE, JSON.stringify(state, null, 2), 'utf8');
}

function main() {
  log('CSA Loom — local golden-path (config-only demo profile)');

  // 1. env.local ------------------------------------------------------------
  step('Ensuring apps/fiab-console/.env.local');
  const env = ensureEnvLocal();
  const secret = env.SESSION_SECRET;
  if (!secret) {
    console.error('  ERROR: SESSION_SECRET could not be resolved from .env.local');
    process.exit(1);
  }

  // 2. mint local session + self-test (round-trip decode) -------------------
  step('Minting a local dev session cookie');
  const claims = {
    oid: 'loom-local-dev-oid',
    name: 'Loom Local Dev',
    email: 'local-dev@loom.localhost',
    upn: 'local-dev@loom.localhost',
    groups: [LOCAL_ADMIN_GROUP],
  };
  const cookie = mintCookie(secret, claims, TTL_SECS);
  // Self-test: prove the cookie decodes back with the SAME secret (this is the
  // exact operation getSession() performs). A failure here means the profile
  // would NOT sign you in — surface it loudly rather than shipping vaporware.
  const decoded = decodeCookie(secret, cookie);
  if (decoded.claims.upn !== claims.upn || decoded.exp < Date.now() / 1000) {
    console.error('  ERROR: minted cookie failed round-trip decode — aborting.');
    process.exit(1);
  }
  writeStorageState(cookie);
  log(`  signed-in as: ${claims.upn} (tenant-admin via ${LOCAL_ADMIN_GROUP})`);
  log(`  round-trip decode OK (exp ${new Date(decoded.exp * 1000).toISOString()})`);
  log(`  Playwright storage state: ${path.relative(REPO_ROOT, AUTH_STATE)}`);

  // 3. readiness check ------------------------------------------------------
  step('Readiness check (scripts/dev/check-local-profile.mjs)');
  const check = spawnSync('node', [path.join(__dirname, 'check-local-profile.mjs')], { stdio: 'inherit' });

  // 4. how to sign a normal browser in --------------------------------------
  step('Sign a normal browser in (optional)');
  log('  Open the DevTools console at http://localhost:3000 and run:');
  log(`    document.cookie = '${COOKIE_NAME}=${cookie.slice(0, 12)}…; path=/';`);
  log('  (full value is in apps/fiab-console/.auth/loom-local-state.json)');
  log('  Or run the Playwright-driven UAT which loads the storage state automatically:');
  log('    LOOM_URL=http://localhost:3000 LOOM_STORAGE_STATE=apps/fiab-console/.auth/loom-local-state.json pnpm --dir apps/fiab-console exec playwright test');

  if (PREPARE_ONLY) {
    step('Prepared (--prepare-only) — not starting the dev server');
    log('  Start it yourself with:  pnpm --dir apps/fiab-console dev');
    process.exit(check.status === 0 ? 0 : 1);
  }

  if (check.status !== 0) {
    step('Not starting the dev server — required prerequisites are failing (see above)');
    process.exit(1);
  }

  // 5. start next dev -------------------------------------------------------
  step('Starting the console (next dev) at http://localhost:3000');
  log('  Ctrl-C to stop.\n');
  const dev = spawn('pnpm', ['--dir', CONSOLE_ROOT, 'dev'], { cwd: REPO_ROOT, stdio: 'inherit', shell: true });
  dev.on('close', (code) => process.exit(code ?? 0));
}

main();
