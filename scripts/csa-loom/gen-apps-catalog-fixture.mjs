#!/usr/bin/env node
/**
 * Generate the offline apps-catalog fixture used by the visual-tutorial
 * coverage audit (B-9).
 *
 * The apps dimension of the coverage gate is dynamic: the live console derives
 * it from `GET /api/apps-catalog`, which (via its registry-derived backstop)
 * surfaces exactly the set of content bundles that are BOTH
 *   - registered in apps/fiab-console/lib/apps/content-bundles/index.ts
 *     (`listBundleIds()` — REGISTRY keys), and
 *   - have display metadata in
 *     apps/fiab-console/lib/apps/content-bundles/catalog-meta.ts (`CATALOG_META`).
 * (The route skips bundles missing catalog metadata; see route.ts line ~90.)
 *
 * So the offline-auditable expected set is `CATALOG_META.keys ∩ listBundleIds()`.
 * This script PARSES both from source (no hand-maintained list — same
 * source-of-truth contract the capture UAT and check-tutorial-coverage.mjs use)
 * and writes scripts/csa-loom/fixtures/apps-catalog.json in the shape
 * check-tutorial-coverage.mjs's --apps-catalog expects: { "apps": [{ "id" }] }.
 *
 * Run it whenever a curated app is added/removed so the gate stays in sync:
 *   node scripts/csa-loom/gen-apps-catalog-fixture.mjs
 *
 * CI runs it with --check to FAIL if the committed fixture has drifted from
 * source (so a new app can never silently escape the coverage audit):
 *   node scripts/csa-loom/gen-apps-catalog-fixture.mjs --check
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repo = path.resolve(__dirname, '..', '..');
const META = path.join(repo, 'apps', 'fiab-console', 'lib', 'apps', 'content-bundles', 'catalog-meta.ts');
const INDEX = path.join(repo, 'apps', 'fiab-console', 'lib', 'apps', 'content-bundles', 'index.ts');
const OUT_DIR = path.join(__dirname, 'fixtures');
const OUT = path.join(OUT_DIR, 'apps-catalog.json');

const check = process.argv.includes('--check');

/** Parse the `'app-…': { … }` keys out of CATALOG_META in catalog-meta.ts. */
function loadCatalogMetaIds() {
  const src = fs.readFileSync(META, 'utf-8');
  const block = src.match(/export const CATALOG_META[^=]*=\s*\{([\s\S]*)\};/);
  if (!block) throw new Error('could not locate CATALOG_META object in catalog-meta.ts');
  return [...block[1].matchAll(/^\s*['"](app-[a-z0-9-]+)['"]\s*:/gm)].map((m) => m[1]);
}

/**
 * Parse the registered bundle ids from index.ts. REGISTRY is keyed by
 * `<import>.appId`, so we resolve each `[<import>.appId]: <import>,` line back to
 * the import's module specifier (./app-<slug>) — the bundle's appId convention
 * is `app-<slug>` matching the file name, asserted by the bundle's own source.
 * We read the import map then the REGISTRY entries.
 */
function loadBundleIds() {
  const src = fs.readFileSync(INDEX, 'utf-8');
  // import <name> from './app-<slug>';
  const imports = new Map(
    [...src.matchAll(/import\s+(\w+)\s+from\s+'\.\/(app-[a-z0-9-]+)';/g)].map((m) => [m[1], m[2]]),
  );
  // [<name>.appId]: <name>,  inside REGISTRY
  const reg = src.match(/const REGISTRY[^=]*=\s*\{([\s\S]*?)\};/);
  if (!reg) throw new Error('could not locate REGISTRY object in index.ts');
  const ids = [...reg[1].matchAll(/\[(\w+)\.appId\]\s*:/g)]
    .map((m) => imports.get(m[1]))
    .filter(Boolean);
  return ids;
}

const metaIds = new Set(loadCatalogMetaIds());
const bundleIds = loadBundleIds();
// The live route surfaces a catalog doc for a bundle only when CATALOG_META has
// an entry for it (route.ts: `if (!meta) continue;`). So intersect.
const ids = bundleIds.filter((id) => metaIds.has(id)).sort();

if (!ids.length) throw new Error('resolved 0 apps — refusing to write an empty catalog fixture');

const fixture = {
  _comment: 'GENERATED by scripts/csa-loom/gen-apps-catalog-fixture.mjs from CATALOG_META ∩ listBundleIds(). Do not hand-edit — run the generator. Used by check-tutorial-coverage.mjs --apps-catalog.',
  generatedFrom: 'apps/fiab-console/lib/apps/content-bundles/{catalog-meta.ts,index.ts}',
  apps: ids.map((id) => ({ id })),
};
const json = JSON.stringify(fixture, null, 2) + '\n';

if (check) {
  const current = fs.existsSync(OUT) ? fs.readFileSync(OUT, 'utf-8') : '';
  if (current !== json) {
    console.error('FAIL: scripts/csa-loom/fixtures/apps-catalog.json is out of date with source.');
    console.error('Run: node scripts/csa-loom/gen-apps-catalog-fixture.mjs  (and commit the result)');
    process.exit(1);
  }
  console.log(`OK: apps-catalog fixture in sync (${ids.length} apps).`);
} else {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.writeFileSync(OUT, json);
  console.log(`Wrote ${OUT} (${ids.length} apps).`);
}
